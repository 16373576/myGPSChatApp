    LatLng latLng;
    String timeKey;
    LocationAndMessage locationAndMessage;
    ArrayList<LocationAndMessage> locations = new ArrayList();

    //link the database to the application
    private FirebaseDatabase database = FirebaseDatabase.getInstance();
    private DatabaseReference myRef = database.getReference("locations");

    //runs when the send message button is selected in the main activity
    @Override
    protected void onCreate(Bundle savedInstanceState) {
        // adds the background, layout and buttons to the messaging activity
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_sendmessage);
        update = findViewById(R.id.sendButton);
        back = findViewById(R.id.backButton);
        message = findViewById(R.id.messageText);
        startGettingLocations();

        //when the send button is clicked the message entered in the textbox is sent to the database
        update.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View v) {
                typedMessage = message.getText().toString();
                message.getText().clear();

                SimpleDateFormat formatter = new SimpleDateFormat("dd-MM-yyyy HH:mm:ss");
                Date date = new Date();
                timeKey = formatter.format(date);
                locationAndMessage = new LocationAndMessage(latLng.latitude, latLng.longitude, typedMessage, timeKey);

                myRef.child(timeKey).setValue(locationAndMessage);

            }
        });

        //when the back button is clicked the activity is finished and return to home page
        back.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View v) {
                finish();
            }
        });
    }

